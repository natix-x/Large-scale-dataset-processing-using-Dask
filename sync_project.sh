#!/bin/bash

LOCAL_PROJECT_DIR="YOUR LOCAL PROJECT DIRECTORY"

REMOTE_USER="YOUR USERNAME"
REMOTE_HOST="HOSTNAME"
REMOTE_PROJECT_DIR="YOUR REMOTE PROJECT DIRECTORY"

# Synchronization (without virtual environment)
rsync -av --exclude='venv' "$LOCAL_PROJECT_DIR" "${REMOTE_USER}@${REMOTE_HOST}:$REMOTE_PROJECT_DIR"
